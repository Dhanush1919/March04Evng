var x1=10;
var x2=5;
function add(x1,x2)
{
  const a=x1+x2;
  return a;
}
function sub(x1,x2)
{
  const b=x1-x2;
  return b;
}
function mul(x1,x2)
{
  const c=x1*x2;
  return c;
}
function div(x1,x2)
{
  const d=x1/x2;
  return d;
}
function mod(x1,x2)
{
  const e=x1%x2;
  return e;
}
function exp(x1,x2)
{
  const f=x1**x2;
  return f;
}


